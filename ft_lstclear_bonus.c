#include "libft.h"

void
	ft_lstclear(t_list **lst, void (*del)(void*));

void
	ft_lstclear(t_list **lst, void (*del)(void*))
{
	struct s_list	*tmp;

	if (lst)
	{
		while (*lst)
		{
			tmp = (*lst)->next;
			ft_lstdelone(*lst, del);
			(*lst) = tmp;
		}
	}
}
